const month = "march"

switch (month) {
    case "jan":
        console.log("january");
        break;

    case "feb":
        console.log("Feb");
        break;
    case "march":
        console.log("March");
        break;
    case "April":
        console.log("April");
        break;
    case "May":
        console.log("May");
        break;
    default:
        console.log("Default case match");
        break;
}